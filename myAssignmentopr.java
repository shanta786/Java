//Program using assignment operator  = , +=, -=
class myAssignmentopr
{
public static void main(String args[])
{
int x=1;
int y=2;
int z=3;

x+=5;  // x=x+5
y*=4;  //y=y*4
z+=x*y; // z=z+x*y
z%=6;  // z=z%6

System.out.println("x="+x);
System.out.println("y="+y);
System.out.println("z="+z);

}
}




